package arth_operators;

class arth_operators{
	  public static void main(String[] args)
	  {
	    int a=6,b=8;
	    //addition operator
	    System.out.println("a+b="+(a+b));

	      //subtraction operator 
	      System.out.println("a-b="+(a-b));
	    
	      //multiplication operator
	      System.out.println("a*b="+(a*b));

	      //division operator
	      System.out.println("a/b="+(a/b));

	      //modulus operator
	        System.out.println("a%b="+(a%b));
	  }
	}